"""
Underwater Dataset Builder
===========================
Downloads, validates, pairs, and merges UIEB + EUVP + UFO-120 into a
single clean dataset ready for training.

Datasets included:
    UIEB     ~890  pairs   Li et al. 2019  — benchmark quality
    EUVP     ~11k  pairs   Islam et al.    — large scale, diverse
    UFO-120  ~1500 pairs   Islam et al.    — diverse conditions

Target: ~12-13k clean pairs after validation filtering.

Final output structure:
    dataset/
    ├── hazy/        ← all degraded inputs   (renamed to avoid collisions)
    ├── clear/       ← all clean references  (same filenames as hazy/)
    └── preprocessed/ ← generated by preprocess_dataset.py afterward

Split saved to:
    weights/train_split.npy
    weights/val_split.npy
    weights/test_split.npy

GPU context (RTX 5070 8GB):
    ~12-13k pairs at 256px, batch=8, nafnet_vit_small (~17M) fits comfortably.
    Larger dataset would require nafnet_vit_base and gradient checkpointing.

Usage:
    python dataset_builder.py                # download + build everything
    python dataset_builder.py --skip-download # if already downloaded manually
    python dataset_builder.py --split 80 10 10
    python dataset_builder.py --max-pairs 10000  # cap total pairs if needed

Manual download fallback (if auto-download fails):
    UIEB   : https://li-chongyi.github.io/proj_benchmark.html
    EUVP   : http://irvlab.cs.umn.edu/resources/euvp-dataset
    UFO-120: http://irvlab.cs.umn.edu/resources/ufo-120
    Place raw zips in  raw_downloads/  and re-run with --skip-download
"""

