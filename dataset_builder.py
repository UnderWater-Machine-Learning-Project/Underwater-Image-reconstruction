# ── Helpers ────────────────────────────────────────────────────────────────────

def _iter(iterable, desc="", unit=""):
    if HAS_TQDM:
        return tqdm(iterable, desc=desc, unit=unit)
    return iterable


def _laplacian_var(img_bgr: np.ndarray) -> float:
    gray = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2GRAY)
    return float(cv2.Laplacian(gray, cv2.CV_64F).var())


def _is_valid_pair(hazy_path: str, clear_path: str):
    """
    Validate a single hazy/clear pair.
    Returns (is_valid, reason_if_rejected_or_clear_img).
    """
    h = cv2.imread(hazy_path)
    c = cv2.imread(clear_path)

    if h is None: return False, "hazy unreadable"
    if c is None: return False, "clear unreadable"

    # Size must match (paired datasets should match; sanity check)
    if h.shape[:2] != c.shape[:2]:
        # Resize clear image to match hazy
        c = cv2.resize(c, (h.shape[1], h.shape[0]))

    # Clear reference quality checks
    if c.mean() < MIN_MEAN:   return False, f"near-black clear (mean={c.mean():.1f})"
    if c.mean() > MAX_MEAN:   return False, f"overexposed clear (mean={c.mean():.1f})"
    if _laplacian_var(c) < MIN_LAPLACIAN:
        return False, f"blurry clear (lap_var={_laplacian_var(c):.1f})"

    # Hazy input checks
    if h.mean() < MIN_MEAN:   return False, f"near-black hazy (mean={h.mean():.1f})"

    return True, c


def _stable_hash(path: str) -> str:
    """Short hash of filename for unique renaming."""
    return hashlib.md5(path.encode()).hexdigest()[:8]


# ── Dataset discovery ──────────────────────────────────────────────────────────

def _find_raw_dataset(name: str) -> Path | None:
    """
    Look for a raw dataset folder inside raw_downloads/.
    Accepts: raw_downloads/UIEB/, raw_downloads/uieb/, raw_downloads/UIEB.zip, etc.
    """
    base = Path(RAW_DIR)
    if not base.exists():
        return None
    for p in base.iterdir():
        if name.lower() in p.name.lower():
            if p.is_dir():
                return p
            if p.suffix.lower() == ".zip":
                return p
    return None


def _extract_zip(zip_path: Path, dest: Path):
    print(f"  Extracting {zip_path.name} → {dest}")
    with zipfile.ZipFile(zip_path, 'r') as z:
        z.extractall(dest)


def _collect_pairs_from_dir(hazy_dir: Path,
                             clear_dir: Path) -> list[tuple[str, str]]:
    """
    Collect (hazy_path, clear_path) pairs by matching filenames.
    Tries exact match first, then stem-only match (different extensions).
    """
    if not hazy_dir.exists() or not clear_dir.exists():
        return []

    clear_by_stem = {}
    for f in clear_dir.iterdir():
        if f.suffix.lower() in VALID_EXTS:
            clear_by_stem[f.stem.lower()] = str(f)

    pairs = []
    for f in hazy_dir.iterdir():
        if f.suffix.lower() not in VALID_EXTS:
            continue
        match = clear_by_stem.get(f.stem.lower())
        if match:
            pairs.append((str(f), match))

    return pairs


# ── Per-dataset loaders ────────────────────────────────────────────────────────

def load_uieb(root: Path) -> list[tuple[str, str]]:
    cfg = DATASETS["UIEB"]
    hazy_dir  = root / cfg["hazy_subdir"]
    clear_dir = root / cfg["clear_subdir"]
    pairs = _collect_pairs_from_dir(hazy_dir, clear_dir)
    print(f"  UIEB: found {len(pairs)} pairs in {hazy_dir.name}/{clear_dir.name}")
    return pairs


def load_euvp(root: Path) -> list[tuple[str, str]]:
    cfg = DATASETS["EUVP"]
    all_pairs = []

    # Primary pair dirs
    h = root / cfg["hazy_subdir"]
    c = root / cfg["clear_subdir"]
    p = _collect_pairs_from_dir(h, c)
    all_pairs.extend(p)
    print(f"  EUVP: {cfg['hazy_subdir'].split('/')[-2]} → {len(p)} pairs")

    # Extra pair dirs
    for hd, cd in cfg.get("extra_pairs", []):
        h2 = root / hd
        c2 = root / cd
        p2 = _collect_pairs_from_dir(h2, c2)
        all_pairs.extend(p2)
        print(f"  EUVP: {hd.split('/')[-2]} → {len(p2)} pairs")

    print(f"  EUVP total: {len(all_pairs)} pairs")
    return all_pairs


def load_ufo120(root: Path) -> list[tuple[str, str]]:
    cfg = DATASETS["UFO-120"]
    hazy_dir  = root / cfg["hazy_subdir"]
    clear_dir = root / cfg["clear_subdir"]
    pairs = _collect_pairs_from_dir(hazy_dir, clear_dir)
    print(f"  UFO-120: found {len(pairs)} pairs in {hazy_dir.name}/{clear_dir.name}")
    return pairs


LOADERS = {
    "UIEB":    load_uieb,
    "EUVP":    load_euvp,
    "UFO-120": load_ufo120,
}


# ── Validation ─────────────────────────────────────────────────────────────────

def validate_pairs(pairs: list[tuple[str, str]],
                   dataset_name: str):
    """
    Run quality checks on all pairs. Returns only clean pairs.
    """
    valid   = []
    rejected = []

    for hp, cp in _iter(pairs, desc=f"Validating {dataset_name}", unit="pair"):
        ok, reason_or_img = _is_valid_pair(hp, cp)
        if ok:
            valid.append((hp, reason_or_img))
        else:
            rejected.append((hp, cp, reason_or_img))

    print(f"  {dataset_name}: {len(valid)} valid / {len(rejected)} rejected")
    if rejected:
        by_reason = {}
        for _, _, r in rejected:
            by_reason[r.split('(')[0].strip()] = by_reason.get(r.split('(')[0].strip(), 0) + 1
        for r, count in sorted(by_reason.items(), key=lambda x: -x[1]):
            print(f"    {count:>4}×  {r}")

    return valid


# ── Merge into dataset/ ────────────────────────────────────────────────────────

def merge_into_dataset(all_pairs,
                       max_pairs: int = None) -> int:
    """
    Copy all validated pairs into dataset/hazy/ and dataset/clear/.
    Files are renamed with a dataset-source prefix + original stem to avoid
    collisions between datasets (e.g. UIEB_000001.jpg, EUVP_000001.jpg).

    Returns number of pairs successfully written.
    """
    os.makedirs(HAZY_DIR,  exist_ok=True)
    os.makedirs(CLEAR_DIR, exist_ok=True)

    if max_pairs:
        np.random.seed(42)
        idx = np.random.choice(len(all_pairs), min(max_pairs, len(all_pairs)),
                               replace=False)
        all_pairs = [all_pairs[i] for i in sorted(idx)]
        print(f"\nCapping to {len(all_pairs)} pairs (--max-pairs)")

    written  = 0
    failed   = 0

    for i, (hp, cp) in enumerate(_iter(all_pairs, desc="Copying pairs", unit="pair")):
        tag  = f"{i:06d}"
        ext  = Path(hp).suffix.lower()
        h_dst = os.path.join(HAZY_DIR,  f"{tag}{ext}")
        c_dst = os.path.join(CLEAR_DIR, f"{tag}{ext}")
        try:
            shutil.copy2(hp, h_dst)
            if isinstance(cp, str):
                shutil.copy2(cp, c_dst)
            else:
                cv2.imwrite(c_dst, cp)
            written += 1
        except Exception as e:
            failed += 1
            if failed <= 5:
                print(f"  [warn] copy failed: {Path(hp).name}: {e}")

    print(f"\nMerge complete: {written} pairs written, {failed} failed")
    return written


# ── Split ──────────────────────────────────────────────────────────────────────

def make_splits(total: int,
                train_pct: float = 0.80,
                val_pct:   float = 0.10,
                test_pct:  float = 0.10):
    """
    Create and save reproducible train/val/test splits.
    Splits are saved as .npy arrays of hazy file paths.
    """
    assert abs(train_pct + val_pct + test_pct - 1.0) < 1e-6, \
        "Train/val/test percentages must sum to 1.0"

    os.makedirs(WEIGHTS_DIR, exist_ok=True)

    exts  = {".jpg", ".jpeg", ".png", ".bmp"}
    files = sorted(
        os.path.join(HAZY_DIR, f)
        for f in os.listdir(HAZY_DIR)
        if Path(f).suffix.lower() in exts)

    np.random.seed(42)
    np.random.shuffle(files)

    n_test  = max(1, int(len(files) * test_pct))
    n_val   = max(1, int(len(files) * val_pct))
    n_train = len(files) - n_test - n_val

    test_files  = files[:n_test]
    val_files   = files[n_test:n_test + n_val]
    train_files = files[n_test + n_val:]

    np.save(os.path.join(WEIGHTS_DIR, "train_split.npy"), np.array(train_files))
    np.save(os.path.join(WEIGHTS_DIR, "val_split.npy"),   np.array(val_files))
    np.save(os.path.join(WEIGHTS_DIR, "test_split.npy"),  np.array(test_files))

    print(f"\nSplit (seed=42):")
    print(f"  Train : {len(train_files):>5}  ({train_pct*100:.0f}%)")
    print(f"  Val   : {len(val_files):>5}  ({val_pct*100:.0f}%)")
    print(f"  Test  : {len(test_files):>5}  ({test_pct*100:.0f}%)")
    print(f"  Total : {len(files):>5}")
    print(f"\n  Splits saved → {WEIGHTS_DIR}/{{train,val,test}}_split.npy")


