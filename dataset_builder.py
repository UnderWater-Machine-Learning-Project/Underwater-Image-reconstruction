# ── Config ─────────────────────────────────────────────────────────────────────

DATASET_DIR   = "dataset"
HAZY_DIR      = os.path.join(DATASET_DIR, "hazy")
CLEAR_DIR     = os.path.join(DATASET_DIR, "clear")
RAW_DIR       = "raw_downloads"
WEIGHTS_DIR   = "weights"

VALID_EXTS    = {".jpg", ".jpeg", ".png", ".bmp"}

# Image quality filters
MIN_LAPLACIAN = 15.0     # Laplacian variance below this = blurry reference
MIN_MEAN      = 15       # Mean pixel value below this = near-black/degenerate
MAX_MEAN      = 245      # Mean above this = overexposed

# Dataset sources — direct download URLs where publicly available
# EUVP and UFO-120 are hosted on irvlab.cs.umn.edu (may require manual DL)
