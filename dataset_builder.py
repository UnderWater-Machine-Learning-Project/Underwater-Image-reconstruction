# ── Main ───────────────────────────────────────────────────────────────────────

def main():
    parser = argparse.ArgumentParser(
        description="Build merged underwater dataset from UIEB + EUVP + UFO-120.")
    parser.add_argument("--skip-download", action="store_true",
                        help="Skip download step — use existing raw_downloads/.")
    parser.add_argument("--split", nargs=3, type=float, default=[80, 10, 10],
                        metavar=("TRAIN", "VAL", "TEST"),
                        help="Split percentages (default: 80 10 10).")
    parser.add_argument("--max-pairs", type=int, default=None,
                        help="Cap total pairs (default: use all valid pairs).")
    parser.add_argument("--datasets", nargs="+",
                        choices=["UIEB", "EUVP", "UFO-120"],
                        default=["UIEB", "EUVP", "UFO-120"],
                        help="Which datasets to include (default: all three).")
    parser.add_argument("--guide", action="store_true",
                        help="Print download instructions and exit.")
    args = parser.parse_args()

    if args.guide:
        print_download_guide()
        return

    # Validate split
    s = args.split
    assert abs(sum(s) - 100) < 0.1, "Split percentages must sum to 100"
    train_p, val_p, test_p = s[0]/100, s[1]/100, s[2]/100

    print("=" * 60)
    print("  Underwater Dataset Builder")
    print("=" * 60)
    print(f"  Datasets : {', '.join(args.datasets)}")
    print(f"  Split    : {s[0]:.0f}/{s[1]:.0f}/{s[2]:.0f}")
    print(f"  Output   : {DATASET_DIR}/")
    print()

    os.makedirs(RAW_DIR, exist_ok=True)

    # ── Collect pairs from each dataset ──────────────────────────────────────
    all_pairs = []

    for name in args.datasets:
        print(f"── {name} ──")
        raw_root = _find_raw_dataset(name)

        if raw_root is None:
            print(f"  [skip] {name} not found in {RAW_DIR}/")
            print(f"         Run: python dataset_builder.py --guide")
            continue

        # Auto-extract zip if needed
        if raw_root.suffix.lower() == ".zip":
            extract_to = Path(RAW_DIR) / name
            _extract_zip(raw_root, extract_to)
            raw_root = extract_to

        # Load pairs
        loader = LOADERS.get(name)
        if loader is None:
            print(f"  [warn] No loader defined for {name}")
            continue

        pairs = loader(raw_root)
        if not pairs:
            print(f"  [warn] No pairs found — check folder structure.")
            print(f"         Run: python dataset_builder.py --guide")
            continue

        # Validate
        valid = validate_pairs(pairs, name)
        all_pairs.extend(valid)
        print()

    if not all_pairs:
        print("\n[error] No valid pairs collected.")
        print_download_guide()
        return

    print(f"\nTotal valid pairs: {len(all_pairs)}")

    # ── VRAM guidance ────────────────────────────────────────────────────────
    n = len(all_pairs) if not args.max_pairs else min(args.max_pairs, len(all_pairs))
    print(f"\nRTX 5070 8GB guidance:")
    if n <= 15000:
        print(f"  {n} pairs → nafnet_vit_small (~17M) at batch=8, IMG_SIZE=256  ✓ fits")
    else:
        print(f"  {n} pairs → consider nafnet_vit_base (~58M) once dataset > 20k")
        print(f"  For now: nafnet_vit_small still recommended until >20k pairs")

    # ── Warn if existing dataset/ has content ────────────────────────────────
    existing_hazy = 0
    if os.path.isdir(HAZY_DIR):
        existing_hazy = len([f for f in os.listdir(HAZY_DIR)
                             if Path(f).suffix.lower() in VALID_EXTS])
    if existing_hazy > 0:
        print(f"\n[warn] dataset/hazy/ already contains {existing_hazy} images.")
        ans = input("  Overwrite? (yes/no): ").strip().lower()
        if ans != "yes":
            print("  Aborted. Use --skip-download to rebuild from existing raw_downloads/.")
            return
        shutil.rmtree(HAZY_DIR,  ignore_errors=True)
        shutil.rmtree(CLEAR_DIR, ignore_errors=True)
        print("  Cleared existing dataset/hazy/ and dataset/clear/")

    # ── Merge ────────────────────────────────────────────────────────────────
    written = merge_into_dataset(all_pairs, max_pairs=args.max_pairs)

    # ── Split ────────────────────────────────────────────────────────────────
    make_splits(written, train_p, val_p, test_p)

    # ── Summary ──────────────────────────────────────────────────────────────
    print("\n" + "=" * 60)
    print("  Dataset build complete!")
    print("=" * 60)
    print(f"""
Next steps:
    1. python preprocess_dataset.py      ← preprocess dataset/hazy/ → dataset/preprocessed/
    2. python train.py                   ← train NAFNet + ViT  (~200 epochs, ~8-12h on 5070)
    3. python main.py --detect-on both   ← compare raw vs enhanced detections
""")


if __name__ == "__main__":
    main()